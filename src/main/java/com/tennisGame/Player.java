package com.tennisGame;

public class Player {

	private String name;

	//-----------------------------------------------------------------------------------------------------------------------//
	//--------------------------------------------------Constructor----------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------------------------------//
	public Player(String name) {
		this.name = name;
		
	}

	//-----------------------------------------------------------------------------------------------------------------------//
	//----------------------------------------------Getters and Setters------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------------------------------//
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
